const typeDefs = gql`
  type Author {
    name: String!
    born: Int
    bookCount: Int!
  }

  type Book {
    title: String!
    published: Int!
    author: Author!
    genres: [String!]!
    id: ID!
  }
  
  type Query {
    authorCount: Int!
    bookCount: Int!
    allBooks(author: String, genre: String): [Book!]!
    allAuthors: [Author!]!
  }

  type Mutation {
    addBook(
      title: String!
      published: Int!
      author: String!
      genres: [String!]!
    ): Book
    editAuthor(
      name: String!,
      setBornTo: Int
    ): Author
  }
`

const resolvers = {
  Query: {
    authorCount: () => authors.length,
    bookCount: () => books.length,
    allBooks: (root, args) => {
      if (!args) return books

      return books
        .filter(b => args.author ? b.author === args.author : true)
        .filter(b => args.genre ? b.genres.find(g => g === args.genre) : true)
    },
    allAuthors: () => authors,
  },
  Author: {
    bookCount: root => (books.filter(b => b.author === root.name)).length
  },
  Mutation: {
    addBook: async (root, args) => {
      const existingBooks = await Book.find({})
      if (existingBooks.map(b => b.title).includes(args.title)) {
        throw new UserInputError('title must be unique', {
          invalidArgs: args.title
        })
      }

      const author = await Author.findOne({ name: args.author })
      let newAuthor
      if (!author) {
        newAuthor = new Author({ name: args.author })
        await newAuthor.save()
      }
      const book = new Book({ ...args, author: author || newAuthor })
      await book.save()
      return book
    },
    editAuthor: (root, args) => {
      if (!authors.find(a => a.name === args.name)) return null
      
      const author = {
        name: args.name,
        born: args.setBornTo,
      }

      authors = authors.map(a => a.name === author.name ? author : a)
      return author
    }
  }
}

const server = new ApolloServer({
  typeDefs,
  resolvers,
})

server.listen().then(({ url }) => {
  console.log(`Server ready at ${url}`)
})


